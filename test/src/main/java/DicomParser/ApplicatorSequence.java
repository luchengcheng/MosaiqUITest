package DicomParser;

public class ApplicatorSequence {
    public String applicatorId = "";
    public String applicatorType = "";

    public String getApplicatorId() {
        return applicatorId;
    }

    public String getApplicatorType() {
        return applicatorType;
    }

    public void setApplicatorId(String applicatorId) {
        this.applicatorId = applicatorId;
    }

    public void setApplicatorType(String applicatorType) {
        this.applicatorType = applicatorType;
    }
}
